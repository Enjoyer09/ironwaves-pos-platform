// ── iRonWaves POS — Retirement / placeholder service worker ────────────────
// This file exists only as a fallback in development mode.
// In production, vite-plugin-pwa generates a real Workbox-based caching SW
// that REPLACES this file in the dist/ output.
//
// Purpose: If any browser tab still has an old cached SW registered from a
// previous deploy, this retirement script will cleanly unregister it and
// delete stale caches so the new Workbox SW can take over on next load.
// ────────────────────────────────────────────────────────────────────────────

self.addEventListener('install', () => {
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil((async () => {
    try {
      const keys = await caches.keys();
      // Delete any caches left by previous SW versions
      await Promise.all(keys.map((key) => caches.delete(key)));
    } catch {
      // Ignore cache cleanup errors — non-fatal
    }
    await self.clients.claim();
    // Self-unregister so the Workbox SW generated by vite-plugin-pwa can
    // register cleanly on the next page load.
    await self.registration.unregister();
  })());
});

self.addEventListener('fetch', () => {
  // Intentionally empty — this retirement SW does not intercept any requests.
});
